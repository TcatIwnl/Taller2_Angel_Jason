    /**
     * Obtiene el multiplicador de efectividad entre dos tipos Pokémon.
     * 
     * @param tipoAtacante Tipo del Pokémon atacante.
     * @param tipoDefensor Tipo del Pokémon defensor.
     * @return Multiplicador correspondiente según la tabla de tipos.
     */
    public static double obtenerEfectividad(String tipoAtacante, String tipoDefensor) {
        int fila = -1;
        int columna = -1;

        for (int i = 0; i < TIPOS.length; i++) {
            if (TIPOS[i].equalsIgnoreCase(tipoAtacante)) fila = i;
            if (TIPOS[i].equalsIgnoreCase(tipoDefensor)) columna = i;
        }

        if (fila != -1 && columna != -1) {
            return EFECTIVIDAD[fila][columna];
        }

        return 1.0;
    }
